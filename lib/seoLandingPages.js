export function getSeoLandingPageBySlug(slug) {
  return SEO_LANDING_PAGES.find((page) => page.slug === slug) || null;
}

export function getRelatedSeoLandingPages(slug) {
  const page = getSeoLandingPageBySlug(slug);

  if (!page) {
    return [];
  }

  return (page.relatedSlugs || [])
    .map((relatedSlug) => getSeoLandingPageBySlug(relatedSlug))
    .filter(Boolean);
}
